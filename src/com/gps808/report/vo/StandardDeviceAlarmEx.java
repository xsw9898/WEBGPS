  public void setDevAlarmEx(StandardDeviceAlarm devAlarm, String toMap)
  {
    this.guid = devAlarm.getGuid();
    this.did = devAlarm.getDevIdno();
    this.vid = devAlarm.getVehiIdno();
    this.stm = devAlarm.getArmTimeStart();
    this.etm = devAlarm.getArmTimeEnd();
    this.atp = devAlarm.getArmType();
    this.info = devAlarm.getArmInfo();
    this.p1 = devAlarm.getParam1();
    this.p2 = devAlarm.getParam2();
    this.p3 = devAlarm.getParam3();
    this.p4 = devAlarm.getParam4();
    this.desc = devAlarm.getArmDesc();
    this.img = devAlarm.getImgInfo();
    this.hd = devAlarm.getHandleStatus();
    this.hdu = devAlarm.getHandleuser();
    this.hdt = devAlarm.getHandleTime();
    this.hdc = devAlarm.getHandleContent();
    
    String[] statusStart = handleFieldData(devAlarm.getStatusStart());
    String[] statusEnd = handleFieldData(devAlarm.getStatusEnd());
    if ((statusStart != null) && (statusStart.length > 0)) {
      this.ss1 = Integer.valueOf(Integer.parseInt(statusStart[0]));
    }
    if ((statusEnd != null) && (statusEnd.length > 0)) {
      this.es1 = Integer.valueOf(Integer.parseInt(statusEnd[0]));
    }
    if ((statusStart != null) && (statusStart.length > 1)) {
      this.ss2 = Integer.valueOf(Integer.parseInt(statusStart[1]));
    }
    if ((statusEnd != null) && (statusEnd.length > 1)) {
      this.es2 = Integer.valueOf(Integer.parseInt(statusEnd[1]));
    }
    if ((statusStart != null) && (statusStart.length > 4)) {
      this.ssp = Integer.valueOf(Integer.parseInt(statusStart[4]));
    }
    if ((statusEnd != null) && (statusEnd.length > 4)) {
      this.esp = Integer.valueOf(Integer.parseInt(statusEnd[4]));
    }
    if ((statusStart != null) && (statusStart.length > 9)) {
      this.slc = Integer.valueOf(Integer.parseInt(statusStart[9]));
    }
    if ((statusEnd != null) && (statusEnd.length > 9)) {
      this.elc = Integer.valueOf(Integer.parseInt(statusEnd[9]));
    }
    if ((statusStart != null) && (statusStart.length > 6))
    {
      this.slng = Integer.valueOf(Integer.parseInt(statusStart[5]));
      this.slat = Integer.valueOf(Integer.parseInt(statusStart[6]));
      
      GpsValue gpsValue = ConvertUtil.convert(this.slng, this.slat, toMap);
      this.smlng = gpsValue.getMapJingDu();
      this.smlat = gpsValue.getMapWeiDu();
    }
    if ((statusEnd != null) && (statusEnd.length > 5) && (statusEnd.length > 6))
    {
      this.elng = Integer.valueOf(Integer.parseInt(statusEnd[5]));
      this.elat = Integer.valueOf(Integer.parseInt(statusEnd[6]));
      
      GpsValue gpsValue = ConvertUtil.convert(this.elng, this.elat, toMap);
      this.emlng = gpsValue.getMapJingDu();
      this.emlat = gpsValue.getMapWeiDu();
    }
    String[] handleInfo = handleFieldData(devAlarm.getHandleInfo());
    if (handleInfo != null)
    {
      if ((handleInfo.length > 0) && (this.hdu == null) && (!handleInfo[0].equals("0"))) {
        this.hdu = handleInfo[0];
      }
      if ((handleInfo.length > 1) && (this.hdt == null)) {
        this.hdt = handleInfo[1];
      }
      if ((handleInfo.length > 2) && (this.hdc == null)) {
        this.hdc = handleInfo[2];
      }
    }
  }
  
  private String[] handleFieldData(String fieldStr)
  {
    if ((fieldStr != null) && (!fieldStr.isEmpty())) {
      return fieldStr.split("\\|");
    }
    return null;
  }
