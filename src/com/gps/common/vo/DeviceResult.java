package com.gps.common.vo;

public class DeviceResult
{
  private String name;
  private String idno;
  private String simCard;
  private String driveName;
  private String driveTele;
  private Integer chnCount;
  
  public String getName()
  {
    return this.name;
  }
  
  public void setName(String name)
  {
    this.name = name;
  }
  
  public String getIdno()
  {
    return this.idno;
  }
  
  public void setIdno(String idno)
  {
    this.idno = idno;
  }
  
  public String getSimCard()
  {
    return this.simCard;
  }
  
  public void setSimCard(String simCard)
  {
    this.simCard = simCard;
  }
  
  public String getDriveName()
  {
    return this.driveName;
  }
  
  public void setDriveName(String driveName)
  {
    this.driveName = driveName;
  }
  
  public String getDriveTele()
  {
    return this.driveTele;
  }
  
  public void setDriveTele(String driveTele)
  {
    this.driveTele = driveTele;
  }
  
  public Integer getChnCount()
  {
    return this.chnCount;
  }
  
  public void setChnCount(Integer chnCount)
  {
    this.chnCount = chnCount;
  }
}
