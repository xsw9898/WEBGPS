  {
    this.sourceIdno = sourceIdno;
  }
  
  public String getTypeIdno()
  {
    return this.typeIdno;
  }
  
  public void setTypeIdno(String typeIdno)
  {
    this.typeIdno = typeIdno;
  }
  
